    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Queue fila = new Queue();
        Player player = new Player();

        // criando personagem
        player.setName(tfNomeUser.getText());
        player.setPassword(tfPassword.getText());

        // adicionando esse personagem na minha fila
        fila.add(player);
        fila.peek();

        //banco de dados
        try {
            Connection con = Conexao.faz_conexao();

            String sql = "select * from dados_players where name=? and senha=?";

            PreparedStatement stmt = con.prepareStatement(sql);

            stmt.setString(1, tfNomeUser.getText());
            stmt.setString(2, new String(tfPassword.getPassword()));

            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                Player playerLogado = new Player();
                playerLogado.setIdPlayer(rs.getInt("id"));
                playerLogado.setName(rs.getString("name"));
                playerLogado.setBalance(rs.getInt("balance"));

System.out.println("Player logado: " + playerLogado.getName() + ", saldo: " + playerLogado.getBalance());

                int id_player = rs.getInt("id");
                System.out.println("ID do player logado: " + id_player);

                HomeCharacter LoginFrame = new HomeCharacter(id_player);
                LoginFrame.setVisible(true);
                LoginFrame.pack();
                LoginFrame.setLocationRelativeTo(null);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Esse usuario n√£o existe");
            }

            stmt.close();
            con.close();

            tfNomeUser.setText("");
            tfPassword.setText("");

        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

