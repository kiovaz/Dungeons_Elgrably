    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (checkBoxCavaleiro.isSelected() && checkBoxMago.isSelected()) {
            JOptionPane.showMessageDialog(null, "Selecione apenas um tipo de personagem");
            return;
        }

        //TODO criar playercharacter
        //TODO adicionar na fila
        
        //banco de dados
        try {
            Connection con = Conexao.faz_conexao();

            if (checkBoxCavaleiro.isSelected()) {
                // convertendo imagem
                try {
                    File arquivo = new File("src/images/cavaleiro .jpg");
                    fis = new FileInputStream(arquivo);
                    tamanho = (int) arquivo.length();
                } catch (Exception e) {
                    System.out.println(e);
                }
                
                String sql = "insert into dados_playercharacter (player_id, character_name, nivel, vida_maxima, vida_atual, manaMaxima, manaAtual, type, foto, agilidade, forca) "
                        + "values (?, ?, 1, 10, 10, 0, 0, 'Cavaleiro', ?, 2, 5)";
                
                

                PreparedStatement stmt = con.prepareStatement(sql);
                stmt.setInt(1, id_player);
                stmt.setString(2, tfNomePers.getText());
                stmt.setBlob(3, fis, tamanho);
                stmt.execute();
                stmt.close();
            }

            if (checkBoxMago.isSelected()) {
                 try {
                    File arquivo = new File("src/images/mago .jpg");
                    fis = new FileInputStream(arquivo);
                    tamanho = (int) arquivo.length();
                } catch (Exception e) {
                    System.out.println(e);
                }
                
                String sql = "insert into dados_playercharacter (player_id, character_name, nivel, vida_maxima, vida_atual, manaMaxima, manaAtual, type, foto, agilidade, forca) "
                        + "values (?, ?, 1, 8, 8, 5, 2, 'Mago', ?, 5, 2)";

                PreparedStatement stmt = con.prepareStatement(sql);
                stmt.setInt(1, getId_player());
                stmt.setString(2, tfNomePers.getText());
                stmt.setBlob(3, fis, tamanho);
                stmt.execute();
                stmt.close();

            }

            con.close();
            JOptionPane.showMessageDialog(null, "Personagem cadastrado com sucesso!");
            tfNomePers.setText("");
            checkBoxCavaleiro.setSelected(false);
            checkBoxMago.setSelected(false);

            HomeCharacter LoginFrame = new HomeCharacter(id_player);
            LoginFrame.setVisible(true);
            LoginFrame.pack();
            LoginFrame.setLocationRelativeTo(null);
            this.dispose();

        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

